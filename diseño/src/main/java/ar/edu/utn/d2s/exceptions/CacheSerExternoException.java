package ar.edu.utn.d2s.exceptions;

public class CacheSerExternoException extends Exception {

	
	private static final long serialVersionUID = 1L;

	public CacheSerExternoException() {
		super();
		
	}

	public CacheSerExternoException(String message, Throwable cause,
			boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	
	}

	public CacheSerExternoException(String message, Throwable cause) {
		super(message, cause);
		
	}

	public CacheSerExternoException(String message) {
		super(message);
	
	}

	public CacheSerExternoException(Throwable cause) {
		super(cause);
		
	}	
	
	
}
